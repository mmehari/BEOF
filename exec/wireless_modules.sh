#!/bin/bash

# This small script is used to find all existing wireless modules (drivers)
# with in the Ubuntu/Debian operating system
#
# It explicitly checks wireless devices connected to the PCI and USB buses.
# First it checks for devices within /sys/bus/[pci/usb]/devices directory.
# Next if devices do not exist, it looks inside /proc/bus/[pci/usb]/devices directory.
#
# After a matching file system (sysfs or procfs),
# 
# 

# Which file system (sysfs/procfs) holds the PCI devices directory
if [ -d "/sys/bus/pci/devices" ] && [ -d "/sys/bus/usb/devices" ]; then
	FS="sys"
elif [ -d "/proc/bus/pci/devices" ] && [ -d "/proc/bus/usb/devices" ]; then
	FS="proc"
else
	echo "No matching file system for PCI and USB kernel devices" >&2
	exit
fi

# Make sure module alias file exists inside the kernel tree
kmod_path="/lib/modules/$(uname -r)/modules.alias"
if ! [ -f $kmod_path ]; then
	echo "Unable to find 'module.alias' inside the kernel tree" >&2
	exit
fi

# Construct alias array for pci modules
modalias_array=( $(cat `find -L /sys/bus/pci/devices/ /sys/bus/usb/devices/ -maxdepth 2 -name '*modalias' -print`) )

# Sort unique values of module aliases
for (( i = 0 ; i < ${#modalias_array[@]} ; i++ )) do
	# PCI modules
	if [[ ${modalias_array[i]:0:3} == "pci" ]]; then
		modalias_array[i]="${modalias_array[i]:0:22}"
	# USB modules
	elif [[ ${modalias_array[i]:0:3} == "usb" ]]; then
		modalias_array[i]="${modalias_array[i]:0:14}"
	fi
done
modalias_array=( $(printf "%s\n" "${modalias_array[@]}" | sort -u) )

